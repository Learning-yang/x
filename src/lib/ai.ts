function pickDemoNotes(req: BatchRequest): Note[] {
  const pool = DEMO_BATCH_POOL[req.industry];
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  const picked = shuffled.slice(0, req.count);
  return picked.map((n) => ({
    ...n,
    type: req.style || n.type,
    title: req.topic ? n.title.replace(/黄黑皮|早八|油皮/gi, req.topic) : n.title
  }));
}

async function callDeepSeek<T>(systemPrompt: string, userPrompt: string): Promise<T> {
  const config = getDeepSeekConfig();
  const response = await fetch(`${config.baseUrl}/chat/completions`, {
    method: "POST",
    headers: {
      "Content-Type": "application/json",
      Authorization: `Bearer ${config.apiKey}`
    },
    body: JSON.stringify({
      model: config.model,
      messages: [
        { role: "system", content: systemPrompt },
        { role: "user", content: userPrompt }
      ],
      temperature: 0.9,
      response_format: { type: "json_object" }
    })
  });
  if (!response.ok) {
    const errorText = await response.text();
    throw new Error(`DeepSeek API 错误: ${response.status} ${errorText}`);
  }
  const data = await response.json();
  const content = data?.choices?.[0]?.message?.content;
  if (!content) {
    throw new Error("DeepSeek 返回内容为空");
  }
  try {
    return JSON.parse(content) as T;
  } catch (err) {
    throw new Error(`解析 DeepSeek 返回的 JSON 失败: ${(err as Error).message}`);
  }
}

export interface BatchGenerateResult {
  notes: Note[];
  demo: boolean;
}

export async function generateBatch(req: BatchRequest): Promise<BatchGenerateResult> {
  if (isDemoMode()) {
    return {
      notes: pickDemoNotes(req),
      demo: true
    };
  }
  const { system, user } = buildBatchPrompt(req);
  const result = await callDeepSeek<{ notes: Note[] }>(system, user);
  if (!Array.isArray(result.notes)) {
    throw new Error("AI 返回的格式不正确");
  }
  return { notes: result.notes, demo: false };
}

